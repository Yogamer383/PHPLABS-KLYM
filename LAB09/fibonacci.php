<?php
// Варіант 9 – Виведення чисел Фібоначчі до 20

// Максимальне значення, до якого обчислюються числа Фібоначчі
$max = 20;

// Початкові два числа послідовності
$f0 = 0;
$f1 = 1;

// Вивести перші два числа
echo $f0 . " ";
echo $f1 . " ";

// Обчислювати наступні числа, доки вони не перевищать $max
while (true) {
    $fn = $f0 + $f1;
    if ($fn > $max) {
        break;
    }
    echo $fn . " ";
    $f0 = $f1;
    $f1 = $fn;
}
?>