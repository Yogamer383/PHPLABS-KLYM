<?php
// Варіант 9 – Розрахунок площі трикутника за формулою Герона
// Довжини сторін трикутника
$a = 6;
$b = 8;
$c = 10;

// Обчислення напівпериметра
$s = ($a + $b + $c) / 2;

// Формула Герона для знаходження площі
$area = sqrt($s * ($s - $a) * ($s - $b) * ($s - $c));

// Виведення результатів
echo "Triangle sides: $a, $b, $c\n";
echo "Semi‑perimeter (s) = $s\n";
echo "Area = $area\n";
?>